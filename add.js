  function addToCart(id) {
    let cart = JSON.parse(localStorage.getItem("cart")) || [];
    const item = menuItems.find(m => m.id === id);

    if (item) {
      cart.push(item);
      localStorage.setItem("cart", JSON.stringify(cart));

      // Format price as INR
      const formattedINR = item.price.toLocaleString('en-IN', {
        style: 'currency',
        currency: 'INR'
      });

      alert(`${item.name} added to cart! Price: ${formattedINR}`);
    } else {
      alert("Item not found!");
    }
  }

